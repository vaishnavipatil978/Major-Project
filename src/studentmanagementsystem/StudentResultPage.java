    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = rn.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM student INNER JOIN result WHERE student.rollNo="+rollNo+ " AND result.rollNo="+rollNo);
 
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Roll No. doesn't exist! ");
            }
            else{
                nam.setText(rs.getString(1));
                gen.setText(rs.getString(3));
                fat.setText(rs.getString(4));
                cou.setText(rs.getString(5));
                bran.setText(rs.getString(6));
                phy.setText(rs.getString(8));
                math.setText(rs.getString(9));
                chem.setText(rs.getString(10));
                ele.setText(rs.getString(11));
                bio.setText(rs.getString(12));
                total.setText(rs.getString(13));
                rn.setEditable(false);
                
                int m1 = Integer.parseInt(rs.getString(8));
                int m2 = Integer.parseInt(rs.getString(9));
                int m3 = Integer.parseInt(rs.getString(10));
                int m4 = Integer.parseInt(rs.getString(11));
                int m5 = Integer.parseInt(rs.getString(12));
                
                if(m1<30 || m2<30 || m3<30 || m4<30 || m5<30){
                    ver.setText("Fail");
                }
                else{
                ver.setText("Pass");
                }
                
                nam.setEditable(false);
                gen.setEditable(false);
                fat.setEditable(false);
                cou.setEditable(false);
                bran.setEditable(false);
                
                phy.setEditable(false);
                math.setEditable(false);
                chem.setEditable(false);
                ele.setEditable(false);
                bio.setEditable(false);
                
                total.setEditable(false);
                ver.setEditable(false);
            }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
    }//GEN-LAST:event_formComponentShown

