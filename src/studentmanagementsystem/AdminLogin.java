    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        String username = user.getText();
        String password = pass.getText();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM login WHERE username='"+username+"' ;" );
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null," User doesn't exits ! ");
            }
            else{
            
                String check = rs.getString(2);
                
                if(password.equals(check)){
                    setVisible(false);
                    AdminHomePage frame = new AdminHomePage();
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else{
                    JOptionPane.showMessageDialog(null, " Entered username/password is incorrect");
                 
                }
            }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
        
        
        
    }//GEN-LAST:event_loginActionPerformed

