    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        // TODO add your handling code here:
        String username = user.getText();
        String password = pass1.getText();
        String confirmPassword = pass2.getText();
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM login WHERE username='"+username+"' ;" );
            
            if(!rs.first()){
                if(password.equals(confirmPassword)){
                    Statement setUser = con.createStatement();
                    setUser.executeUpdate("Insert into login values('"+username+"','"+password+"')");
                    JOptionPane.showMessageDialog(null, "Successfully Added!");
                    setVisible(false);
                    AdminLogin frame = new AdminLogin();
                    frame.setVisible(true);
                    frame.setLocationRelativeTo(null);
                }
                else{
                    JOptionPane.showMessageDialog(null," Passwords don't match ! ");
                }
            }
            else{
            
                JOptionPane.showMessageDialog(null, " Username Already Exits ! ");
            }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
    }//GEN-LAST:event_createActionPerformed

